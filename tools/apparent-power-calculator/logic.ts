const HISTORY_KEY = "apparent-power-calculator-history";
const MAX_HISTORY = 10;

/**
 * Calculate apparent power
 */
export function calculateApparentPower(inputs: ApparentPowerInputs): ApparentPowerResult {
  const { voltage, current } = inputs;
  
  // Calculate apparent power: S = V × I
  const apparentPower = voltage * current;
  
  // Generate step-by-step calculation
  const steps = [
    `Step 1: Identify the values`,
    `Voltage (V) = ${voltage} V`,
    `Current (I) = ${current} A`,
    ``,
    `Step 2: Apply the formula`,
    `Apparent Power (S) = Voltage × Current`,
    `S = V × I`,
    ``,
    `Step 3: Calculate`,
    `S = ${voltage} × ${current}`,
    `S = ${apparentPower} VA`,
    ``,
    `Result: ${apparentPower} VA`
  ];
  
  return {
    apparentPower,
    voltage,
    current,
    formula: "S = V × I",
    steps,
    timestamp: Date.now()
  };
}

/**
 * Validate inputs
 */
export function validateInputs(inputs: ApparentPowerInputs): string | null {
  const { voltage, current } = inputs;
  
  if (isNaN(voltage) || voltage <= 0) {
    return 'Voltage must be greater than 0';
  }
  
  if (isNaN(current) || current <= 0) {
    return 'Current must be greater than 0';
  }
  
  return null;
}

